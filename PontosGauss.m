function [x, w] = PontosGauss(n)
% Função que determina quais são os pontos para Quadratura de Gauss-Legendre
switch n
    case 1
        w = 2;
        x = 0;
    case 2
        w(1) = 1;
        w(2) = w(1);
        x(1) = -1 / sqrt(3);
        x(2) = -x(1);
        w = w';
        x = x';
    case 3
        w(1) = 5/9;
        w(2) = 8/9;
        w(3) = w(1);
        x(1) = -sqrt(15)/5;
        x(2) = 0;
        x(3) = -x(1);
        w = w';
        x = x';
    case 4
        w(1) = (18 - sqrt(30)) / 36;
        w(2) = (18 + sqrt(30)) / 36;
        w(3) = w(2);
        w(4) = w(1);
        x(1) = -sqrt(525 + 70*sqrt(30)) / 35;
        x(2) = -sqrt(525 - 70*sqrt(30)) / 35;
        x(3) = -x(2);
        x(4) = -x(1);
        w = w';
        x = x';
    case 5
        w(1) = (322 - 13*sqrt(70))/900;
        w(2) = (322 + 13*sqrt(70))/900;
        w(3) = 128/225;
        w(4) = w(2);
        w(5) = w(1);
        x(1) = -sqrt(245 + 14*sqrt(70)) / 21;
        x(2) = -sqrt(245 - 14*sqrt(70)) / 21;
        x(3) = 0;
        x(4) = -x(2);
        x(5) = -x(1);
        w = w';
        x = x';
    case 6
        w(1) = 0.171324492379170;
        w(2) = 0.360761573048139;
        w(3) = 0.467913934572691;
        w(4) = 0.467913934572691;
        w(5) = 0.360761573048139;
        w(6) = 0.171324492379170;
        x(1) = -0.932469514203152;
        x(2) = -0.661209386466265;
        x(3) = -0.238619186083197;
        x(4) = -x(3);
        x(5) = -x(2);
        x(6) = -x(1);
        w = w';
        x = x';
    case 7
        w(1) = 0.129484966168870;
        w(2) = 0.279705391489281;
        w(3) = 0.381830050505130;
        w(4) = 0.417959183673470;
        w(5) = 0.381830050505107;
        w(6) = 0.279705391489273;
        w(7) = 0.129484966168869;
        x(1) = -0.9491079123428;
        x(2) = -0.7415311855994;
        x(3) = -0.4058451513774;
        x(4) =  0.0000000000000;
        x(5) =  0.4058451513774;
        x(6) =  0.7415311855994;
        x(7) =  0.9491079123428;
        w = w';
        x = x';
end
end





